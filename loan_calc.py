# برنامج لحساب القسط الشهري للقرض
import math

print("---------------------------------------")
print("حاسبة القسط الشهري للقرض")
print("---------------------------------------")

# 1. إدخال المتغيرات من المستخدم
P = float(input("أدخل قيمة المبلغ المطلوب قرضه: "))
r_annual = float(input("أدخل معدل الفائدة السنوي (على شكل عشري 0.05 مثلاً): "))
t_years = float(input("أدخل عدد سنوات القرض: "))

# 2. حساب المتغيرات الشهرية
r_monthly = r_annual / 12  # معدل الفائدة الشهري
n_months = t_years * 12      # عدد الأقساط الكلي

# 3. تطبيق صيغة حساب القسط الشهري (M)
# M = P [ i(1 + i)^n ] / [ (1 + i)^n – 1]
M = P * (r_monthly * math.pow(1 + r_monthly, n_months)) / (math.pow(1 + r_monthly, n_months) - 1)

# 4. طباعة النتائج النهائية مع التقريب
print("\n--- النتائج ---")
print("القسط الشهري هو:", round(M, 2))
